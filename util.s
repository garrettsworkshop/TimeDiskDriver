setup_pointers:
; Store CX00 pointers in $90-91, $92-93, ... $9E-$9F
LDA #$00
STA PTR_C800
STA PTR_C900
STA PTR_CA00
STA PTR_CB00
STA PTR_CC00
STA PTR_CD00
STA PTR_CE00
STA PTR_CF00
LDA #$C8
STA PTR_C800+1
LDA #$C9
STA PTR_C900+1
LDA #$CA
STA PTR_CA00+1
LDA #$CB
STA PTR_CB00+1
LDA #$CC
STA PTR_CC00+1
LDA #$CD
STA PTR_CD00+1
LDA #$CE
STA PTR_CE00+1
LDA #$CF
STA PTR_CF00+1
; Zero CTAB low pointer
LDA #$00
STA PTR_CTAB
; Return
RTS
